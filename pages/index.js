import Head from "next/head";
import useSWR from "swr";
import styled from "styled-components";
import { StyledLink } from "../components/StyledLink.js";
import DataGridComponent from "../components/TableDataGrid";
import Profile from "../components/profile";
import { useSession } from "next-auth/react";
import { useState } from "react";

const List = styled.ul`
  list-style: none;
  display: flex;
  flex-direction: column;
  align-items: center;
  gap: 1rem;
  padding-left: 0;
`;

const ListItem = styled.li`
  position: relative;
  width: 100%;
`;
const FixedLink = styled(StyledLink)`
  position: fixed;
  bottom: 50px;
  right: 50px;
`;

export default function Home() {
  const { data: session } = useSession();
  const [searchQuery, setSearchQuery] = useState("");

  let apiUrl;
  if (session) {
    apiUrl = `/api/services?userId=${session?.user.userId}`;
  } else {
    apiUrl = `/api/services`;
  }

  const { data } = useSWR(apiUrl, {
    fallbackData: [],
  });

  if (!data) return;

  const filteredData = data.filter(
    (item) =>
      // fields to search by
      item.firstName?.toLowerCase().includes(searchQuery.toLowerCase()) ||
      item.lastName?.toLowerCase().includes(searchQuery.toLowerCase()) ||
      item.userName?.toLowerCase().includes(searchQuery.toLowerCase())
  );

  return (
    <>
      <Head>
        <title>Parcelio App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="initial-scale=1, width=device-width" />

        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Profile />
      <main>
        <h1>Parcelio - send your parcel through Kyrgyz community ðŸ‡°ðŸ‡¬</h1>

        <input
          type="text"
          placeholder="Search users..."
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
          style={{ margin: "10px 0", padding: "5px", width: "30%" }}
        />
        {/* <DataGridComponent data={data} /> */}
        <DataGridComponent data={filteredData} />
      </main>
    </>
  );
}
